// For loop
document.write(("For Loop")+'<br>');
for(i=1; i<=5; i++){
    document.write("Hello" + "<br>");
}
document.write("<br>");

// While Loop
document.write(("While Loop")+ '<br>');
var i = 1;
while(i<=5){
    document.write(i + "<br>");
    i++;
}
document.write("<br>")

var i = 10;
while(i >=1){
    document.write(i + "<br>");
    i--;
}
document.write("<br>")



